nome='Giovanni' #Variabile che contiene il nome (non inserito dall'utente)
print('Ciao ', nome) #Funzione che stampa nel terminale la scritta 'Ciao Giovanni' (Giovanni equivale alla variabile 'nome')
print() #Funzione per aggiungere una riga vuota nel terminale
print('Vogliamo sapere se ti piace programmare!') #Funzione che stampa nel terminale una domanda all'utente
print('Ti piace programmare?') #Funzione che scrive nel terminale una domanda per l'utente
risposta=input() #Funzione che prende la risposta dell'utente come variabile
print('Bene, hai scritto ', risposta) #Funzione che scrive nel terminale 'Bene, hai scritto: (risposta aggiunta precedentemente)'